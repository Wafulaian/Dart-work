import 'package:dart_application_1/dart_application_1.dart' as dart_application_1;

void main(List<String> arguments) {
  // Integer (int) to store whole numbers
  int age = 30;
  print("Age: $age");

  // Double (double) to store decimal numbers
  double pi = 3.14159;
  print("Pi: $pi");

  // String (String) to store text
  String name = "John Doe";
  print("Name: $name");

  // List (List) to store an ordered collection of items
  List<String> colors = ["red", "green", "blue"];
  print("Colors: $colors");

  // Map (Map) to store key-value pairs
  Map<String, int> fruits = {"apple": 10, "banana": 5};
  print("Fruits: $fruits");
}
